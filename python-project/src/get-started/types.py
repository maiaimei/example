#!/usr/bin/python
# -*- coding: UTF-8 -*-
"""
Python 数据类型
"""

# 整型
my_int = 1

# 浮点型
my_float = 1.1

# 字符串
# Python 可以使用引号( ' )、双引号( " )、三引号( ''' 或 """ ) 来表示字符串，引号的开始与结束必须是相同类型的。
my_string_1 = "Hello, World"
my_string_2 = 'Hello, Python'
my_string_3 = '''Hello, World!!!
Hello, Python!!!
'''
my_string_4 = """
Hello, World!!!
Hello, Python!!!
"""

# 布尔型
my_boolean_1 = True
my_boolean_2 = False
print(my_boolean_1)
print(my_boolean_2)
print(int(my_boolean_1))  # True->1
print(int(my_boolean_2))  # False->0
